// Random number generator class definition

#include "Random.h"

//=============================================================================
// Constructor 
// Seeds srand() using the system clock
//=============================================================================
Random::Random()
{
	srand(time(&time_seed));
	seed = 0;
}

//=============================================================================
// Constructor
// Seeds srand() using the given value
//=============================================================================
Random::Random(int i)
{
	seed = i;
	srand(seed);
	time_seed = 0;
}

//=============================================================================
// Destructor
//=============================================================================
Random::~Random()
{
	// NULL
}

//=============================================================================
// Generates a random number
//=============================================================================
int Random::rNum()
{
	return rand();
}